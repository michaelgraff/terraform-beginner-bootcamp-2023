// package main: Declares the package name
// the main package is special in Go, it is where the execution of the program starts
package main

// import "fmt": imports the fmt (format) package, which contains functions for formatted I/O
import "fmt"

// func main(): defines the main function, the entry point of the application.
// when you run the program, it starts executing from this function
func main() {
	//Format.PrintLine and prints to stdout
	fmt.Println("Hello, world!")
}